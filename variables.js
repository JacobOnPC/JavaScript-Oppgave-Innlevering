// Her viser jeg eksempel med de forksjellige operatorene//

let levelUp = 10;
let powerLevel = 9001;
let multiplyMe = 32;
let quarterMe = 1152;

// Gir de matematiske oppgavene i feltet nedenfor:
levelUp += 5;
powerLevel -= 100;
multiplyMe *= 11;
quarterMe /= 4;


// Disse console.log()-setningene nedenfor vil hjelpe deg å sjekke verdiene til variablene.
// Du trenger ikke å redigere disse setningenes. 
console.log('The value of levelUp:', levelUp); 
console.log('The value of powerLevel:', powerLevel); 
console.log('The value of multiplyMe:', multiplyMe); 
console.log('The value of quarterMe:', quarterMe);



